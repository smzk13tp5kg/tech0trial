GIT_OVERVIEW = """
## 🔵 Git と OneDrive の違い（固定表示）

### 1. 管理する単位が違う
**OneDrive**：ファイル単体の過去版を保存（最大500版）  
**Git**：フォルダ全体の“その瞬間の状態”を保存  
→ 履歴は無制限  

---

### 2. 壊したときに戻せる範囲が違う
**OneDrive**  
- 消したファイルだけ戻せる  
- フォルダ全体は戻らない  

**Git**  
- フォルダ全体を過去状態に戻せる  
- うっかり削除しても安全  

---

### 3. 履歴の作られ方が違う
**OneDrive**：修正前のファイルコピーを丸ごと保存  
**Git**：  
- 変わった部分だけ新しく保存  
- 変わらない部分は過去のデータを再利用  
→ それでも「フォルダ丸ごとのスナップショット」が残る
"""

